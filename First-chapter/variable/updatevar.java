public class updatevar {
    public static void main(String[] args) {
        int number = 11;
        String text = "Ruby";
        System.out.println(number);
        System.out.println(text);

        //update variable number
        number = 9;

        //Cetak
        System.out.println(number);

        //Update variable text
        text = "Java";

        //Cetak
        System.out.println(text);
    }
}
